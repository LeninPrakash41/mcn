#!/usr/bin/env python3
"""
MCN Full-Stack App Generator - Phase 4
Generate complete applications with MCN backend and React frontend
"""

import os
import json
import argparse
from typing import Dict, List, Any
from pathlib import Path


class MCNAppGenerator:
    def __init__(self):
        self.templates_dir = Path(__file__).parent / "templates"

    def create_app(
        self, app_name: str, template: str = "business", features: List[str] = None
    ):
        """Create a new MCN full-stack application"""
        print(f"Creating MCN app: {app_name}")
        print(f"Template: {template}")

        app_dir = Path(app_name)
        if app_dir.exists():
            print(f"Error: Directory {app_name} already exists")
            return 1

        # Create directory structure
        self._create_directory_structure(app_dir)

        # Generate backend MCN files
        self._generate_backend(app_dir, template, features or [])

        # Generate React frontend
        self._generate_frontend(app_dir, app_name, template)

        # Generate configuration files
        self._generate_config_files(app_dir, app_name)

        print(f"SUCCESS MCN app '{app_name}' created successfully!")
        print(f"\nNext steps:")
        print(f"  cd {app_name}")
        print(f"  npm install")
        print(f"  npm run dev")

        return 0

    def generate_model(self, model_name: str, fields: Dict[str, str]):
        """Generate MCN model with CRUD operations"""
        print(f"Generating model: {model_name}")

        # Generate MCN backend model
        model_mcn = self._generate_model_mcn(model_name, fields)

        # Write to backend directory
        backend_dir = Path("backend")
        if not backend_dir.exists():
            backend_dir.mkdir()

        model_file = backend_dir / f"{model_name.lower()}.mcn"
        with open(model_file, "w") as f:
            f.write(model_mcn)

        print(f"SUCCESS Model {model_name} generated at {model_file}")
        return 0

    def generate_api(self, resource_name: str, crud: bool = True):
        """Generate API endpoints for resource"""
        print(f"Generating API for: {resource_name}")

        api_mcn = self._generate_api_mcn(resource_name, crud)

        # Write to backend directory
        backend_dir = Path("backend")
        if not backend_dir.exists():
            backend_dir.mkdir()

        api_file = backend_dir / f"{resource_name.lower()}_api.mcn"
        with open(api_file, "w") as f:
            f.write(api_mcn)

        print(f"SUCCESS API {resource_name} generated at {api_file}")
        return 0

    def generate_ui(self, component_name: str, model_name: str = None):
        """Generate React UI component"""
        print(f"Generating UI component: {component_name}")

        component_tsx = self._generate_component_tsx(component_name, model_name)

        # Write to frontend directory
        frontend_dir = Path("frontend/src/components")
        if not frontend_dir.exists():
            frontend_dir.mkdir(parents=True)

        component_file = frontend_dir / f"{component_name}.tsx"
        with open(component_file, "w") as f:
            f.write(component_tsx)

        print(f"SUCCESS Component {component_name} generated at {component_file}")
        return 0

    def _create_directory_structure(self, app_dir: Path):
        """Create application directory structure"""
        directories = [
            app_dir,
            app_dir / "backend",
            app_dir / "frontend" / "src" / "components",
            app_dir / "frontend" / "src" / "services",
            app_dir / "frontend" / "src" / "pages",
            app_dir / "frontend" / "public",
            app_dir / "config",
            app_dir / "docs",
        ]

        for directory in directories:
            directory.mkdir(parents=True, exist_ok=True)

