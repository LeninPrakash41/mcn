def main():
    parser = argparse.ArgumentParser(description="MCN Full-Stack App Generator")
    subparsers = parser.add_subparsers(dest="command", help="Available commands")

    # New app command
    new_parser = subparsers.add_parser("new", help="Create new MCN app")
    new_parser.add_argument("name", help="Application name")
    new_parser.add_argument(
        "--template", default="business", help="App template (default: business)"
    )
    new_parser.add_argument(
        "--features", nargs="*", help="Additional features (auth, analytics, etc.)"
    )

    # Generate model command
    model_parser = subparsers.add_parser("generate-model", help="Generate model")
    model_parser.add_argument("name", help="Model name")
    model_parser.add_argument(
        "--fields", help="Fields in format name:type,email:string,age:number"
    )

    # Generate API command
    api_parser = subparsers.add_parser("generate-api", help="Generate API")
    api_parser.add_argument("name", help="Resource name")
    api_parser.add_argument(
        "--crud", action="store_true", help="Include CRUD operations"
    )

    # Generate UI command
    ui_parser = subparsers.add_parser("generate-ui", help="Generate UI component")
    ui_parser.add_argument("name", help="Component name")
    ui_parser.add_argument("--model", help="Associated model name")

    args = parser.parse_args()

    if not args.command:
        parser.print_help()
        return 1

    generator = MCNAppGenerator()

    if args.command == "new":
        return generator.create_app(args.name, args.template, args.features)
    elif args.command == "generate-model":
        fields = {}
        if args.fields:
            for field_def in args.fields.split(","):
                name, field_type = field_def.split(":")
                fields[name] = field_type
        return generator.generate_model(args.name, fields)
    elif args.command == "generate-api":
        return generator.generate_api(args.name, args.crud)
    elif args.command == "generate-ui":
        return generator.generate_ui(args.name, args.model)
    else:
        print(f"Unknown command: {args.command}")
        return 1


if __name__ == "__main__":
    exit(main())
